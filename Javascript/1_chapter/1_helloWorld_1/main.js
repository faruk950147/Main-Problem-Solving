// ******************************* Arithmetic Operators in JavaScript ******************************
// File: 1_chapter/arithmeticOperators/main.js
console.log(5+10); //Addition
console.log(5-10); //Subtraction
console.log(5*10); //Multiplication
console.log(5/10); //Division
console.log(5%10); //Modulus
console.log(5**10); //Exponentiation
console.log(5 / 10); //Floor Division

// ********************************* Increment and Decrement Operators in JavaScript ******************************
// pre-increment and pre-decrement post-increment and post-decrement
let x = 5;
console.log(++x); // Pre-increment: x is incremented before being used
console.log(x);   // x is now 6
console.log(--x); // Pre-decrement: x is decremented before being used
console.log(x);   // x is now 5
console.log(x++); // Post-increment: x is used before being incremented
console.log(x);   // x is now 6
console.log(x--); // Post-decrement: x is used before being decremented 
console.log(x);   // x is now 5
