# ********************************* Increment and Decrement Operators in JavaScript ******************************
x = 5

# Post-increment (Python style)
print(x)   # Output: 5
x += 1

print(x)   # Output: 6

# Pre-decrement
x -= 1
print(x)   # Output: 5

print(x)   # Output: 5

# Post-increment
print(x)   # Output: 5
x += 1

print(x)   # Output: 6

# Post-decrement
print(x)   # Output: 6
x -= 1

print(x)   # Output: 5
