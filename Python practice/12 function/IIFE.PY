# IIEF  (Immediately Invoked Function Expression) for concept javascript

print((lambda x: x * x)(5))
print((lambda x,y: x + y)(5, 3))
print((lambda x,y: x - y)(5, 3))
print((lambda x,y: x * y)(5, 3))
print((lambda x,y: x / y)(5, 3))
print((lambda x,y: x % y)(5, 3))
print((lambda x,y: x // y)(5, 3))
print((lambda x,y: x ** y)(5, 3))
