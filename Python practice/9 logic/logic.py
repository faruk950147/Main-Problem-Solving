# ***************************** > ei dike thakle left boro hote hobe tahle true hobe ***************************
print(20 > 20) # False 20 ki 20 er cheye boro na ki 
print(20 > 10) # True 20 ki 10 er cheye boro na ki 

# ***************************** < ei dike thakle left choto hote hobe tahle true hobe ***************************
print(10 < 20) # True 10 ki 20 er cheye choto na ki 
print(20 < 10) # False 20 ki 10 er cheye choto na ki

# ***************************** == ei dike thakle left and right same hote hobe tahle true hobe ***************************
print(20 == 20) # True 20 ki 20 er cheye same na ki 

# ***************************** != ei dike thakle left and right same na hote hobe tahle true hobe ***************************
print(20 != 20) # False 20 ki 20 er cheye same na ki 

# ***************************** >= ei dike thakle left boro hote hobe tahle true hobe ***************************
print(20 >= 20) # True 20 ki 20 er cheye boro na ki 
print(20 >= 10) # False 20 ki 10 er cheye boro na ki 

# ***************************** <= ei dike thakle left choto hote hobe tahle true hobe ***************************
print(20 <= 20) # True 20 ki 20 er cheye choto na ki 
print(20 <= 10) # False 20 ki 10 er cheye choto na ki 


# ***************************** and ei dike thakle left and right true hote hobe tahle true hobe ***************************
print(20 > 10 and 20 < 30) # True 20 ki 10 er cheye boro na ki 
print(20 > 10 and 20 > 30) # False 20 ki 10 er cheye boro na ki 

# ***************************** or ei dike thakle left or right true hote hobe tahle true hobe ***************************
print(20 > 10 or 20 < 30) # True 20 ki 10 er cheye boro na ki 
print(20 > 10 or 20 > 30) # True 20 ki 10 er cheye boro na ki

