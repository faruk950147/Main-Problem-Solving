# +x is identity operator and return same value
# -x is negation operator and return opposite value
# ~x is bitwise NOT operator and return Complement value

a = 10
print(+a)
# output
# 10
print(-a)
# output
# -10
print(~a)
# output
# Binary of 10:     00001010
# Bitwise NOT:      11110101  (2's complement of -11)
# Decimal: -11
